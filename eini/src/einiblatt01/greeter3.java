package einiblatt01;

public class greeter3 {
	public static void main(String[] args) {
		int a = 3, b = 4;
		int c = a * b;

		int d = 8, e = 2;
		int f = d + e;

		System.out.println(c); // gibt das erste ergebnis in einer zeile aus
		System.out.println(f); // gibt das zweite ergebnis in einer eigenen zeile aus
	}
}
